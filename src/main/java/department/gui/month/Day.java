package department.gui.month;

import java.util.List;

public class Day {
	
	private String name;
	private List<String> days;
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public List<String> getDays() {
		return days;
	}
	
	public void setDays(List<String> days) {
		this.days = days;
	}
}
